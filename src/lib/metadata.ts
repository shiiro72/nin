export function calculateRV(substats: { type: string; value: number }[]): number {
  return substats.reduce((total, sub) => {
    const maxRoll = MAX_STAT_ROLLS[sub.type];
    if (maxRoll) {
      return total + (sub.value / maxRoll) * 100;
    }
    return total;
  }, 0);
}

